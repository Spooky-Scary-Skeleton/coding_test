x = int(input())

#최소 연산횟수 기록용 리스트
dp = [1e9] * 30001

dp[1] = 0
dp[2] = 1
dp[3] = 1
dp[5] = 1


#각 연산별 비교
for i in range(x + 1):
  if i % 2 == 0:
    dp[i] = min(dp[i//2] + 1, dp[i-1] + 1)
  if i % 3 == 0:
    dp[i] = min(dp[i//3] + 1, dp[i], dp[i-1] + 1)
  if i % 5 == 0:
    dp[i] = min(dp[i//5] + 1, dp[i], dp[i-1] + 1)

print(dp[x])


x = int(input())

count = 0

while x != 1:
  while x % 5 != 0 and x % 3 != 0 and x % 2 != 0:
    x -= 1
    count += 1

  while x % 5 == 0:
    x /= 5
    count += 1

  while x % 3 == 0:
    x /= 3
    count += 1

  while x % 2 == 0:
    x /= 2
    count += 1

print(count)


